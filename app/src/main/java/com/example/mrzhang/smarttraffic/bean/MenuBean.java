package com.example.mrzhang.smarttraffic.bean;

public class MenuBean {
    int iv;
    String tv;

    public MenuBean() {
    }

    public MenuBean(int iv, String tv) {
        this.iv = iv;
        this.tv = tv;
    }

    public int getIv() {
        return iv;
    }

    public void setIv(int iv) {
        this.iv = iv;
    }

    public String getTv() {
        return tv;
    }

    public void setTv(String tv) {
        this.tv = tv;
    }
}
